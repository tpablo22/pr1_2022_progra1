package dominio;

public class Libreta {

	private String nombre;
	private int edad;


	public int getEdad() {
		return edad;
	}

	public void setEdad(int edad) {
		this.edad = edad;
	}

	public String getNombre() {

		return nombre;
	}

	public void setNombre(String nombre) {

		this.nombre = nombre;
	}

	public String toString() {
		return nombre;
	}
}
